const generateDiaries = (clientId: string, weeks: number): SleepDiary[] => {
  const diaries: SleepDiary[] = [];
  const start = new Date("2026-06-10");
  for (let i = weeks * 7 - 1; i >= 0; i--) {
    const d = new Date(start);
    d.setDate(d.getDate() - i);
    const dateStr = d.toISOString().split("T")[0];
    const isWeekend = d.getDay() === 0 || d.getDay() === 6;
    const submitted = Math.random() > 0.2;
    const bedHour = 22 + Math.floor(Math.random() * 3);
    const bedMin = Math.floor(Math.random() * 60);
    const wakeHour = 6 + Math.floor(Math.random() * 2);
    const wakeMin = Math.floor(Math.random() * 60);
    const wakeDrift = Math.floor(Math.random() * 90) - 15;
    diaries.push({
      id: `d-${clientId}-${i}`,
      clientId,
      date: dateStr,
      bedTime: `${String(bedHour).padStart(2, "0")}:${String(bedMin).padStart(2, "0")}`,
      sleepOnset: 10 + Math.floor(Math.random() * 50),
      wakeTime: `${String(wakeHour).padStart(2, "0")}:${String(wakeMin).padStart(2, "0")}`,
      totalSleepTime: 5 + Math.floor(Math.random() * 3),
      sleepEfficiency: 65 + Math.floor(Math.random() * 30),
      nightAwakenings: Math.floor(Math.random() * 4),
      weekendCatchUp: isWeekend && Math.random() > 0.6,
      daytimeNap: isWeekend ? (Math.random() > 0.5 ? Math.floor(Math.random() * 120) : 0) : (Math.random() > 0.8 ? Math.floor(Math.random() * 40) : 0),
      wakeDrift,
      submitted,
      notes: submitted && Math.random() > 0.7 ? "今日状态尚可" : undefined,
    });
  }
  return diaries;
};

